from turtle import Turtle
import random

class Food(Turtle):

# whenever you call a new class everything in the init gets executed
    def __init__(self):
        super().__init__() # we are creating a Food class, and inheriting averything from the Turtle class
        self.shape("circle")
        self.penup()
        self.shapesize(stretch_wid=0.5, stretch_len=0.5)
        self.color("blue")
        self.speed("fastest")
        random_x = random.randint(-280, 280)
        random_y = random.randint(-280, 280)
        self.goto(random_x, random_y)


    def refresh(self):
        random_x = random.randint(-280, 280)
        random_y = random.randint(-280, 280)
        self.goto(random_x, random_y)